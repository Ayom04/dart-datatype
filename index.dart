void main() {
  // Integer (int) data type
  int age = 25;
  print('Age: $age');

  // Double data type
  double height = 5.9;
  print('Height: $height');

  // String data type
  String name = 'John Doe';
  print('Name: $name');

  // List data type
  List<String> fruits = ['Apple', 'Banana', 'Orange'];
  print('Fruits: $fruits');

  // Map data type
  Map<String, int> scores = {
    'Math': 90,
    'Science': 85,
    'English': 95,
  };
  print('Scores: $scores');
}
